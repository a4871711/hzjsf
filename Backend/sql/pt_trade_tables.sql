-- 私教 · 交易域建表(购买/权益/预约)
-- pt_private_order, pt_private_order_coupon_rel, pt_member_private_benefit, pt_private_appointment
-- 依据《私教需求文档》第21节 DDL + 《失历健身私教管理后台_详细实现文档》总则0.4/各域§2
-- 字符集 utf8mb4；标 -- 【补充】 的列/索引为需求DDL未给、实现所需(评审项)
